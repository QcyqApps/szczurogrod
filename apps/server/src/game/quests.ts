export function getQuestTemplate(id: string): QuestTemplate | null {
  return REGISTRY.quests.get(id) ?? null;
}

function filterPoolByClass<T extends { allowedClasses?: readonly CharacterClass[] | null }>(
  pool: readonly T[],
  cls: CharacterClass,
): readonly T[] {
  return pool.filter((t) => !t.allowedClasses || t.allowedClasses.includes(cls));
}

/** Roll a quest loot drop for a given difficulty + player class. */
export function rollLoot(
  diff: QuestDifficulty,
  itemChance: number,
  cls: CharacterClass,
): ItemTemplate | null {
  if (Math.random() * 100 >= itemChance) return null;
  const items = REGISTRY.questLoot.get(diff) ?? [];
  const pool = filterPoolByClass(items, cls);
  if (pool.length === 0) return null;
  return pool[Math.floor(Math.random() * pool.length)];
}

/** Boss unique drop for a specific quest + class. Returns null if not a boss quest. */
export function getBossUniqueDrop(
  questId: string,
  cls: CharacterClass,
): ItemTemplate | null {
  return REGISTRY.bossDrops.get(questId)?.[cls] ?? null;
}
