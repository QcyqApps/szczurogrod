export interface LootTemplate extends Omit<LootItem, 'id'> {
  slot: ItemSlot;
  atk?: number;
  def?: number;
  mag?: number;
  desc: string;
  /**
   * Classes that can loot this template. Undefined = universal (any class).
   * Weapons are typically restricted: melee (sword/dagger/axe/club) → warrior+rogue;
   * casters (orb/staff) → mage. Armor/potions/trinkets stay universal.
   */
  allowedClasses?: readonly CharacterClass[];
}

